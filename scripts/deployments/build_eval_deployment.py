def build_eval_artifact(root: Path, pipeline_id: str) -> Path:
    manifest = load_manifest(root, pipeline_id)
    logical = manifest["logical_dag_id"]
    eval_version = int(manifest["current_eval_version"])
    version_file = _version_file_for_eval(manifest)

    versions_path = root / "pipelines" / pipeline_id / "versions" / version_file
    if not versions_path.exists():
        raise FileNotFoundError(f"Missing candidate version file: {versions_path}")

    artifact_dir = root / ".build" / "eval"
    artifact_dir.mkdir(parents=True, exist_ok=True)
    artifact_path = artifact_dir / _generated_eval_filename(logical, eval_version)

    source = _render_eval_dag_source(logical, eval_version, version_file)
    artifact_path.write_text(source, encoding="utf-8")
    return artifact_path


def _targets(root: Path, pipeline_id: str | None) -> list[str]:
    if pipeline_id:
        return [pipeline_id]
    return list_pipeline_ids(root)


def run(root: Path, pipeline_id: str | None) -> int:
    rc = 0
    for pid in _targets(root, pipeline_id):
        try:
            path = build_eval_artifact(root, pid)
            print(f"[{pid}] built eval artifact: {path}")
        except FileNotFoundError as exc:
            rc = 1
            print(f"[{pid}] build skipped: {exc}")
    return rc


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="build_eval_deployment")
    parser.add_argument("--root", default=".", help="Repository root")
    parser.add_argument("--pipeline-id", default=None, help="Single pipeline id; default all")
    return parser


def main() -> int:
    args = build_parser().parse_args()
    return run(Path(args.root).resolve(), args.pipeline_id)


if __name__ == "__main__":
    raise SystemExit(main())
